public class _005_Operator {
	public static void main(String[] args) {
		//arithmetic operator
		/* [ '+', '-', '*', '/', '%' ] */
		System.out.println(17/3);
		System.out.println(17./3);
		System.out.println((int)(17./3));
		
		//unary operator
		int a = 10;
		System.out.println("a = " + a);
		System.out.println("++a = " + ++a); //prefix operator
		System.out.println("a++ = " + a++); //postfix operator
		System.out.println("a = " + a);
		
		//relational operator
		/* [ '==', '!=', '<', '>', '<=', '>=' ] */
		
		//conditional operator
		/* && - and
		 * || - or
		 * !  - not (negation, unary) */
	}
}